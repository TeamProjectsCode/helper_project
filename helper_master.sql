-- 지역의 처음, 두번째 이름을 이용해서 no를 구해내는 함수
CREATE OR REPLACE FUNCTION GET_LOCATION_NO(location_first_name IN VARCHAR2, location_second_name IN VARCHAR2)
    RETURN NUMBER
    IS
    location_no NUMBER;
BEGIN

    SELECT NO
    INTO location_no
    FROM LOCATIONS
    WHERE FIRST_NAME LIKE location_first_name AND SECOND_NAME LIKE location_second_name;

    return location_no;
END;

-- 구인게시글의 나의 지원 상태를 가져오는 함수
CREATE OR REPLACE FUNCTION GET_VOLUNTEER_STATE(user_no IN NUMBER, job_no IN NUMBER)
    RETURN VARCHAR2
    IS
    user_state VARCHAR2(200);
    st         NUMBER;
BEGIN

    user_state := ' ';

    SELECT STATE
    INTO st
    FROM VOLUNTEERS
    WHERE USER_NO = GET_VOLUNTEER_STATE.user_no
      AND POST_NO = job_no;

    CASE
        WHEN st = 0 THEN
            user_state := '고려중';
        WHEN st = 1 THEN
            user_state := '승인';
        WHEN st = 2 THEN
            user_state := '실패';
        END CASE;

    RETURN user_state;

EXCEPTION
    WHEN OTHERS THEN
        RETURN user_state;
END;


-- 해당 구인게시글의 '승인'이 된 사람 인원수 출력
CREATE OR REPLACE FUNCTION GET_WORKER_COUNT(job_no IN NUMBER)
    RETURN NUMBER
    IS
    worker_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO worker_count FROM VOLUNTEERS WHERE POST_NO = job_no AND STATE = 1;
    return worker_count;
END;

    COMMIT ;

