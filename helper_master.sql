-- public int deleteUser(String no)
CREATE OR REPLACE PROCEDURE DELETE_USER(user_no IN NUMBER)
IS
    user_id VARCHAR2(30);
BEGIN
    SELECT ID INTO user_id FROM USERS WHERE NO = user_no;

    IF user_id IS NOT NULL AND user_id NOT LIKE 'ADMIN_'
       THEN
           DELETE FROM SCRAPS WHERE USER_NO = DELETE_USER.user_no;
           DELETE FROM JOB_BOARD WHERE CREATOR_NO = user_no;
           DELETE FROM REVIEW_BOARD WHERE CREATOR_NO = user_no;
           DELETE FROM PURCHASES WHERE USER_NO = DELETE_USER.user_no;
           DELETE FROM USERS WHERE NO = user_no;
           COMMIT;
   END IF;
END;


